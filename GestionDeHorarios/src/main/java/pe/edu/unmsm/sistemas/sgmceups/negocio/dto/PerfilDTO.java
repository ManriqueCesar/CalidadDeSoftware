package pe.edu.unmsm.sistemas.sgmceups.negocio.dto;

public class PerfilDTO {
	private Integer id;
	private String nombre;
	
	public PerfilDTO() {
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
